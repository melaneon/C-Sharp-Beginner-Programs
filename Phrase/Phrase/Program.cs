using System;

namespace Phrase
{
    class Phrase
    {
        static void Main()
        {
            Console.Write("Laugh often,");
            Console.Write("Dream big,");
            Console.Write("Reach fir the stars!\n\n");

            Console.Write("Laugh often, \nDream big, \nReach fir the stars!\n\n");

            Console.Write("Laugh\n");
            Console.Write("often,\n");
            Console.Write("Dream\n");
            Console.Write("big,\n");
            Console.Write("Reach\n");
            Console.Write("For\n");
            Console.Write("the\n");
            Console.Write("stars!\n\n");

            //OR

            Console.WriteLine("Laugh");
            Console.WriteLine("often,");
            Console.WriteLine("Dream");
            Console.WriteLine("big,");
            Console.WriteLine("Reach");
            Console.WriteLine("For");
            Console.WriteLine("the");
            Console.WriteLine("stars!");

            //For me personally WriteLine() (output 4) method is the most streamlined approach.


        }
    }
}
