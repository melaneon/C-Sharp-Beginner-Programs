using System;

namespace Patterns
{
    class Patterns
    {
        static void Main()
        {
            Console.WriteLine("         00         00");
            Console.WriteLine("        0000       0000");
            Console.WriteLine("         00         00\n\n\n");
            //I use escape sequence character "\n" in this example to move cursor to the next line.
            Console.WriteLine("00                            00");
            Console.WriteLine("  00                        00");
            Console.WriteLine("    00                   00");
            Console.WriteLine("      000             000");
            Console.WriteLine("         0000     0000");
            Console.WriteLine("             00000");

        }
    }
}
